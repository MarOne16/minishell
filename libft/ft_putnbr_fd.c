#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	x;

	x = 0;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		n *= -1;
		x = 1;
	}
	if (x == 1)
		write(fd, "-", 1);
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	write(fd, &"0123456789"[n % 10], 1);
}
